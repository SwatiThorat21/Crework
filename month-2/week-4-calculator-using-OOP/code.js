
class Calculator {
constructor(number1, number2){
    this.number1 = number1;
    this.number2 = number2;
}

 add(number1, number2){
  return number1 + number2;
}

minus(number1, number2){
    return number1 - number2;
  }

multiply(number1, number2){
    return number1 * number2;
  }

division(number1, number2){
    return number1 / number2;
  }  
}


